# Функция для сортировки элементов строк по возрастанию
def sort_rows(matrix):
    for row in matrix:
        row.sort()  # Используем метод сортировки для сортировки элементов в каждой строке
    return matrix

# Пример двумерного массива (можно заменить на свой массив)
array = [
    [4, 2, 7, 1],
    [9, 5, 3, 8],
    [6, 2, 0, 9]
]

# Выводим исходный массив до сортировки
print("Исходный массив:")
for row in array:
    print(row)

# Сортируем элементы строк массива по возрастанию
sorted_array = sort_rows(array)

# Выводим отсортированный массив
print("\nМассив после сортировки элементов строк по возрастанию:")
for row in sorted_array:
    print(row)
